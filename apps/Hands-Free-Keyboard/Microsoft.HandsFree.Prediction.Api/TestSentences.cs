        static string[] CreateUndecoratedSentences()
        {
            var ar = new string[rawSentences.Length];

            for (var i = 0; i < rawSentences.Length; i++)
            {
                var builder = new StringBuilder();

                foreach (var ch in rawSentences[i])
                {
                    if (char.IsLetter(ch) || ch == ' ')
                    {
                        builder.Append(ch);
                    }
                }

                ar[i] = builder.ToString();
            }

            return ar;
        }

